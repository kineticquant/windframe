function exportHTML() {

    const findElements = document.querySelectorAll("#canvas > div");
    //bug fix - if handles are selected while exporting, should force-deselect them
    findElements.forEach(el => {
    const handles = el.querySelectorAll(".resize-handle");
    handles.forEach(handle => {
      handle.style.display = "none";
    });
    el.style.border = "none";
  });

    const canvasContent = document.getElementById("canvas").innerHTML;
  
    // full html structure with tailwind css
    // must include the cdn for tailwind otherwise it will just generate as generic html
    const htmlStruct = `
    <!DOCTYPE html>
    <html lang="en">
    <head>
      <meta charset="UTF-8">
      <meta name="viewport" content="width=device-width, initial-scale=1.0">
      <title>windframe - autogenerated tailwind UI</title>
      <link href="https://cdn.jsdelivr.net/npm/tailwindcss@2.2.19/dist/tailwind.min.css" rel="stylesheet">
    </head>
    <body>
      ${canvasContent}
    </body>
    </html>
    `;
  
    const blob = new Blob([htmlStruct], { type: "text/html" });
    const link = document.createElement("a");
    link.href = URL.createObjectURL(blob);
    link.download = "windframe.html";
    link.click();
  }